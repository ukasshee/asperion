let currentLang = localStorage.getItem("asperion-lang") || "pl";
let currentTheme = localStorage.getItem("asperion-theme") || "light";

function updateThemeLabel() {
  const themeButton = document.querySelector(".theme-toggle");
  if (!themeButton) return;
  const label = currentTheme === "dark" ? translations[currentLang].themeToLight : translations[currentLang].themeToDark;
  themeButton.setAttribute("aria-label", label);
  themeButton.setAttribute("title", label);
}

function setTheme(theme, persist = true) {
  currentTheme = theme;
  document.documentElement.dataset.theme = theme;
  if (persist) localStorage.setItem("asperion-theme", theme);
  document.querySelector('meta[name="theme-color"]').content = theme === "dark" ? "#24150f" : "#fffdf7";
  updateThemeLabel();
}

function setLanguage(lang, persist = true) {
  currentLang = lang;
  if (persist) localStorage.setItem("asperion-lang", lang);
  document.documentElement.lang = lang;
  document.title = translations[lang].title;
  document.querySelector('meta[name="description"]').content = translations[lang].description;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = translations[lang][node.dataset.i18n];
    if (value) node.innerHTML = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((node) => {
    const value = translations[lang][node.dataset.i18nAlt];
    if (value) node.setAttribute("alt", value);
  });

  document.querySelectorAll(".lang").forEach((button) => {
    const active = button.dataset.lang === lang;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", active);
  });
  updateThemeLabel();
  document.dispatchEvent(new CustomEvent("asperion:language", { detail: { lang } }));
}

document.querySelectorAll(".lang").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.querySelector(".theme-toggle")?.addEventListener("click", () => {
  setTheme(currentTheme === "dark" ? "light" : "dark");
});

const toggle = document.querySelector(".menu-toggle");
const links = document.querySelector(".nav-links");
toggle?.addEventListener("click", () => {
  const expanded = toggle.getAttribute("aria-expanded") === "true";
  toggle.setAttribute("aria-expanded", String(!expanded));
  links.classList.toggle("open", !expanded);
  document.body.classList.toggle("menu-open", !expanded);
});

document.querySelectorAll('.nav-links a').forEach((link) => link.addEventListener("click", () => {
  toggle?.setAttribute("aria-expanded", "false");
  links.classList.remove("open");
  document.body.classList.remove("menu-open");
}));

document.querySelector("[data-call-button]")?.addEventListener("click", (event) => {
  const mobileUserAgent = navigator.userAgentData?.mobile;
  const isPhone = typeof mobileUserAgent === "boolean"
    ? mobileUserAgent
    : /Android.+Mobile|iPhone|iPod|Windows Phone|webOS/i.test(navigator.userAgent);

  if (isPhone) return;

  event.preventDefault();
  const button = event.currentTarget;
  const number = document.querySelector("#desktop-phone-number");
  number.hidden = false;
  button.setAttribute("aria-expanded", "true");
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.14 });
document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));

document.querySelector("#contact-form")?.addEventListener("submit", async (event) => {
  event.preventDefault();
  const form = event.currentTarget;
  const status = form.querySelector(".form-status");
  const submit = form.querySelector('button[type="submit"]');
  const data = new FormData(form);

  status.textContent = translations[currentLang].formSending;
  submit.disabled = true;

  try {
    const response = await fetch("/api/contact", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        name: data.get("name"),
        email: data.get("email"),
        topic: data.get("topic"),
        message: data.get("message"),
        website: data.get("website"),
        consent: data.get("consent") === "on",
        lang: currentLang
      })
    });
    const result = await response.json().catch(() => ({}));
    if (!response.ok) {
      const key = result.code === "rate_limit" ? "formRateLimit" : "formError";
      throw new Error(key);
    }
    status.textContent = translations[currentLang].formSuccess;
    form.reset();
  } catch (error) {
    status.textContent = translations[currentLang][error.message] || translations[currentLang].formError;
  } finally {
    submit.disabled = false;
  }
});

